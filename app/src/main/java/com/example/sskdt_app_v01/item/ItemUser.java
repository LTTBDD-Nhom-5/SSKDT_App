package com.example.sskdt_app_v01.item;

public class ItemUser {
    private int img;
    private  String name;

    public ItemUser(int img, String name) {
        this.img = img;
        this.name = name;
    }

    public int getImg() {
        return img;
    }

    public void setImg(int img) {
        this.img = img;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
